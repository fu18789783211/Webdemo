// if语句
// 条件分支语句
if (条件表达式) {
    // 条件为真时，执行的语句
} else {
    // 条件为假时，执行的语句
}
// （多分支的 if 语句）
if (条件表达式1) {
    // 条件1为真时，做的事情

} else if (条件表达式2) {
    // 条件1不满足，条件2满足时，做的事情

} else if (条件表达式3) {
    // 条件1、2不满足，条件3满足时，做的事情

} else {
    // 条件1、2、3都不满足时，做的事情
}



